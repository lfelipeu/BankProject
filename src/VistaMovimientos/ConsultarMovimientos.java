    //Botón Buscar
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /*
        Aquí verifica que el campo texto no esté vacío
        */
        if (jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                "Faltan Datos por Completar",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }else{
            /*
            Aquí llama a la función buscarnumcuenta del modelo datos
            */
            if (md.buscarnummovimiento(Integer.parseInt(jTextField1.getText()))==1){
                JOptionPane.showMessageDialog(null,
                    "Movimiento Encontrado. Se Mostrarán los Datos.",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
                md.mostrardatos(jTextField1.getText(), 
                        jTextField2, jTextField3, 
                        jTextField4, jTextField5, 
                        jTextField6, jTextField7);
                co.desbloquearobjetosconsultar(jButton1, jComboBox1, 
                        jTextField1, jTextField2, 
                        jTextField3, jTextField4, 
                        jTextField5, jTextField6, 
                        jTextField7);
            }else{
                JOptionPane.showMessageDialog(null,
                    "Movimiento No Encontrado",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

