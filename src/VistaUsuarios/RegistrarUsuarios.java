    //Botón Buscar
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /*
        Aquí verifica que el campo texto no esté vacío
        */
        if (jTextField1.getText().equals("") ||
                jPasswordField1.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                "Faltan Datos por Completar",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }else{
            /*
            Aquí llama a la función buscarloginopassword y si la función 
            devuelve 1 es porque alguna de las dos cosas está registrada en la
            tabla
            */
            if (md.buscarloginopassword(jTextField1.getText(),
                    jPasswordField1.getText())==1){
                JOptionPane.showMessageDialog(null,
                    "Login o Password ya Registrados",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
                jTextField1.requestFocus();
            }else{
                JOptionPane.showMessageDialog(null,
                    "Login y Password disponible, Puede Registrar el Usuario",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
                co.desbloquearobjetosregistrar(jButton1, jButton2, 
                        jComboBox1, jComboBox2, 
                        jTextField1, jTextField2, 
                        jTextField3, jPasswordField1);
                jTextField2.requestFocus();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

