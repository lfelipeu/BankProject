    public void ingresardatos(java.awt.event.ActionEvent e){
        //Instancia el form
        RegistrarUsuarios ru = new RegistrarUsuarios();
        //Aquí se verifica que los campos texto tengan datos almacenados
        if (jTextField2.getText().equals("") ||
                jTextField3.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                    "Faltan Datos por Completar",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }else{
            String nivel = "";
            String condicion = "";
            //Asigna a la variable direccion el elemento seleccionado del combo
            nivel = "" + jComboBox1.getSelectedItem();
            condicion = "" + jComboBox2.getSelectedItem();
            /*
            Aquí llama a la funcion para ingresar un cliente en la tablaclientes
            */
            if (md.ingresarusuario(jTextField1.getText(),
                    jPasswordField1.getText(),
                    nivel,
                    jTextField2.getText(),
                    condicion,
                    jTextField3.getText())==true){
                /*
                Llama al método ingresarbitacora del modelo para que se registre
                el movimiento en la bitácora
                */
                mb.ingresarbitacora(f.imprimefecha(), md.ploginusuario, ru.getTitle());
                JOptionPane.showMessageDialog(null,
                    "Usuario Agregado Correctamente",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
                co.limpiarcampostextosregistrar(jTextField1, jTextField2, 
                        jTextField3, jPasswordField1);
                co.bloquearobjetosregistrar(jButton1, jButton2, 
                        jComboBox1, jComboBox2, 
                        jTextField1, jTextField2, 
                        jTextField3, jPasswordField1);
            }else{
                JOptionPane.showMessageDialog(null,
                    "Usuario No Registrado",
                    "Advertencia",
                    JOptionPane.WARNING_MESSAGE);
            }
        }
    }
    
