    public void modifica (java.awt.event.ActionEvent e){
        //Instancia el form
        ModificarUsuarios mu = new ModificarUsuarios();
        //Aquí se verifica que los campos texto tengan datos almacenados
        if (jTextField3.getText().equals("") ||
                jTextField4.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                    "Faltan Datos por Completar",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }else{
            String nuevonivel;
            nuevonivel = "" + jComboBox1.getSelectedItem();
            if(md.modificar(
                    jTextField1.getText(),
                    jPasswordField1.getText(),
                    nuevonivel,
                    Integer.parseInt(jTextField3.getText()),
                    jTextField4.getText())==true){
                /*
                Llama al método ingresarbitacora del modelo para que se registre
                el movimiento en la bitácora
                */
                mb.ingresarbitacora(f.imprimefecha(), md.ploginusuario, mu.getTitle());
                JOptionPane.showMessageDialog(null,
                    "Usuario Modificado Correctamente",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
                co.limpiarcampostextosmodificar(jTextField1, jTextField2, 
                        jTextField3, jTextField4, 
                        jPasswordField1);
                co.bloquearobjetosmodificar(jButton1, jButton2, 
                        jComboBox1, jTextField1,
                        jTextField2, jTextField3, 
                        jTextField4, jPasswordField1);
            }else{
                JOptionPane.showMessageDialog(null,
                    "Usuario No Modificado",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }
    
