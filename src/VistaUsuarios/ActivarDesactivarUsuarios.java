    public void modifica (java.awt.event.ActionEvent e){
        //Instancia el form
        ActivarDesactivarUsuarios au = new ActivarDesactivarUsuarios();
        String condicion;
        condicion = "" + jComboBox1.getSelectedItem();
        //Aquí se verifica que los campos texto tengan datos almacenados
        if (jTextField1.getText().equals("") ||
                jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                    "Faltan Datos por Completar",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }else{
            if(md.modificarcondicion(
                    jTextField1.getText(),
                    jPasswordField1.getText(),
                    condicion)==true){
                /*
                Llama al método ingresarbitacora del modelo para que se registre
                el movimiento en la bitácora
                */
                mb.ingresarbitacora(f.imprimefecha(), md.ploginusuario, au.getTitle());
                JOptionPane.showMessageDialog(null,
                    "Condición Modificada Correctamente",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
                co.limpiarcampostextosactivacion(jTextField1, jTextField2, 
                        jPasswordField1);
                co.bloquearobjetosactivacion(jButton1, jButton2, 
                        jComboBox1, jTextField1, 
                        jTextField2, jPasswordField1);
            }else{
                JOptionPane.showMessageDialog(null,
                    "Condición No Modificada",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }
    
