    //Botón Buscar
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        /*
        Aquí verifica que el campo texto no esté vacío
        */
        if (jTextField1.getText().equals("") ||
                jPasswordField1.getText().equals("")){
            JOptionPane.showMessageDialog(null,
                "Faltan Datos por Completar",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }else{
            /*
            Aquí llama a la función buscarloginypassword y si la función
            devuelve 1 es porque esta registrada
            */
            if (md.buscarloginypassword(jTextField1.getText(),
                jPasswordField1.getText())==1){
                JOptionPane.showMessageDialog(null,
                    "Usuario Encontrado. Se Mostrará su Condición.",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
                md.mostrarcondicion(jTextField1.getText(),
                        jPasswordField1.getText(),
                        jTextField2);
                co.desbloquearobjetosactivacion(jButton1, jButton2, 
                        jComboBox1, jTextField1, 
                        jTextField2, jPasswordField1);
            }else{
                JOptionPane.showMessageDialog(null,
                    "Usuario No Encontrado",
                    "Información",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

