    //Botón Eliminar
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Verifica si la cuenta tiene saldo, si es así no puede eliminarla
        if (Integer.parseInt(jTextField5.getText()) > 0){
            JOptionPane.showMessageDialog(null,
                    "Para Eliminar una Cuenta Esta No Puede Tener Saldo",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }else{
            elimina(evt);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public void elimina(java.awt.event.ActionEvent e){
        //Instancia el formulario
        EliminarCuentas ec = new EliminarCuentas();
        if (JOptionPane.showConfirmDialog(null, 
                "Está seguro(a) de Eliminar Cuenta?")==
                JOptionPane.OK_OPTION){
            /*
            Aquí llama a la función eliminar para que se elimine el cliente de
            la tabla, siempre y cuando la función es igual a true
            */
            if (md.eliminarcuenta(Integer.parseInt(jTextField1.getText()))==true){
                JOptionPane.showMessageDialog(null,
                        "Cuenta Eliminada Correctamente",
                        "Información",
                        JOptionPane.INFORMATION_MESSAGE);
                /*
                Llama al método ingresarbitacora del modelo para que se registre
                el movimiento en la bitácora
                */
                mb.ingresarbitacora(f.imprimefecha(), mu.ploginusuario, ec.getTitle());
                co.limpiarcampostextoseliminar(jTextField1, jTextField2, 
                        jTextField3, jTextField4, 
                        jTextField5, jTextField6, 
                        jTextField7, jTextField8);
                co.bloquearobjetoseliminar(jButton1, jButton2, 
                        jComboBox1, jTextField1, 
                        jTextField2, jTextField3, 
                        jTextField4, jTextField5, 
                        jTextField6, jTextField7, 
                        jTextField8);
            }else{
                JOptionPane.showMessageDialog(null,
                        "Cuenta NO ha Eliminada",
                        "Advertencia",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }
    
